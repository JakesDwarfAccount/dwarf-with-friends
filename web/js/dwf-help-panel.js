// B207 HELP-TOOLTIPS: the ? help panel -- a FULL reference of every tooltip/explanation the client
// UI carries, browsable in one place.
//
// Before this, the top-right "?" button (and F1) only opened the keyboard-shortcut overlay. the
// report: "this is supposed to open up a list of ALL of the tooltips in the game." So the panel
// now renders the whole harvested corpus (web/js/dwf-help-corpus.js, generated by
// tools/harness/help_corpus_extractor.mjs) as one searchable reference:
//   * every surface (Top bar, Buildings/zones, Squads, Labor, Kitchen, ...) is its own section,
//   * DF's first-time context guides are the "Guides" section,
//   * the keyboard-shortcut list is ONE section here (Shift+H still opens the compact hotkey
//     overlay on its own, unchanged -- dwf-keymap.js).
// A search box (the shared DWFUI search idiom) filters every entry live. Curated one-liners
// (web/js/dwf-help-curated.js) may add a dim clarifying sub-line to a terse tooltip without
// forking the harvested text.
//
// Built from DWFUI (headerHtml / searchHtml / scrollHtml / rowHtml) so it inherits the shared
// panel grammar. The render function is PURE (corpus + curated + query -> HTML string) so the
// fixture suite (tools/harness/help_reference_test.mjs) exercises it with no browser.

(function (root) {
  "use strict";

  var DWFUI = root.DWFUI || (typeof require === "function" ? require("./dwf-ui-components.js") : null);

  function esc(s) {
    if (DWFUI && DWFUI.esc) return DWFUI.esc(s);
    return String(s == null ? "" : s)
      .replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
  }

  // ---- corpus helpers (pure) -----------------------------------------------------------------

  // The curated note for a harvested entry, matched EXACTLY on {surface, text}. "" when none.
  function curatedNote(curated, surfaceId, text) {
    var notes = curated && curated.notes && curated.notes[surfaceId];
    return (notes && Object.prototype.hasOwnProperty.call(notes, text)) ? notes[text] : "";
  }

  // Does an entry match the search query? Searches the tooltip text/title, the control name, the
  // hotkey, the group, guide body paragraphs, and any curated note -- so a player can find a
  // tooltip by any word they remember. Empty query matches everything.
  function entryMatches(entry, note, q) {
    if (!q) return true;
    var hay = [
      entry.text, entry.title, entry.control, entry.hotkey, entry.group, note,
      Array.isArray(entry.body) ? entry.body.join(" ") : "",
    ].join(" ").toLowerCase();
    return hay.indexOf(q) !== -1;
  }

  // ---- render (pure) -------------------------------------------------------------------------

  // A hotkey badge is a SHORT native-style token ("Ctrl+Z") -> bitmap text, like every other label.
  function keyBadge(control) {
    return '<span class="help-ref-key">' + DWFUI.bitmapTextHtml(String(control == null ? "" : control)) + "</span>";
  }

  // DF's own first-open guide text, rendered as a PROSE BLOCK (heading + flowing paragraphs) rather
  // than as a row. Bitmap text is a single-run label span, not a flowing text block: pushing wrapped
  // multi-paragraph guide copy through it would produce one unwrappable run per paragraph. The whole
  // block is therefore a DECLARED bypass -- rawHtml() refuses to emit without a plain-English reason,
  // so the exemption is on the record instead of being a silent raw-string leak.
  function guideHtml(entry) {
    var paras = (entry.body || []).map(function (p) { return "<p>" + esc(p) + "</p>"; }).join("");
    return DWFUI.rawHtml(
      "first-open guide text is flowing multi-paragraph prose (h4 + wrapped <p> copy), not a label run",
      '<div class="help-guide"><h4>' + esc(entry.title) + "</h4>" + paras + "</div>");
  }

  function entryRowHtml(surface, entry, note) {
    if (surface.kind === "guides") return guideHtml(entry);

    var isHotkey = surface.kind === "hotkeys";
    // Hotkeys: the control IS the key -> render it as a badge; the text is the description.
    var icon = isHotkey ? keyBadge(entry.control) : "";
    var trailing = (!isHotkey && entry.hotkey)
      ? '<span class="help-ref-hotkey">' + esc(entry.hotkey) + "</span>" : "";
    var sub = note ? { text: note, cls: "help-ref-note" } : null;
    var label = isHotkey ? entry.text : (entry.text || entry.control);
    return DWFUI.rowHtml({
      cls: "help-ref-row" + (isHotkey ? " hk" : ""),
      icon: icon, label: label, sub: sub, trailing: trailing,
    });
  }

  // Group hotkey entries by their `group` (Camera/System/...) so the section keeps DF's own
  // sub-grouping; other surfaces render as a flat list.
  function sectionInnerHtml(surface, curated, q) {
    if (surface.kind === "hotkeys") {
      var groups = [];
      var byGroup = {};
      surface.entries.forEach(function (e) {
        var note = "";
        if (!entryMatches(e, note, q)) return;
        var g = e.group || "";
        if (!byGroup[g]) { byGroup[g] = []; groups.push(g); }
        byGroup[g].push(e);
      });
      if (!groups.length) return "";
      return groups.map(function (g) {
        var rows = byGroup[g].map(function (e) { return entryRowHtml(surface, e, ""); }).join("");
        return (g ? '<div class="help-ref-group">' + esc(g) + "</div>" : "") + rows;
      }).join("");
    }
    var out = surface.entries.map(function (e) {
      var note = curatedNote(curated, surface.id, e.text);
      if (!entryMatches(e, note, q)) return "";
      return entryRowHtml(surface, e, note);
    }).join("");
    return out;
  }

  // Full reference body: one <section> per surface, empty sections dropped (so search collapses
  // the page to only matching surfaces). Returns { html, count } so the caller can show a tally.
  function renderBody(corpus, curated, query) {
    var q = String(query || "").trim().toLowerCase();
    var count = 0;
    var sections = (corpus && corpus.surfaces || []).map(function (surface) {
      var inner = sectionInnerHtml(surface, curated, q);
      if (!inner) return "";
      // count visible entries for the tally
      surface.entries.forEach(function (e) {
        if (entryMatches(e, curatedNote(curated, surface.id, e.text), q)) count++;
      });
      return '<section class="help-ref-section" data-surface="' + esc(surface.id) + '">' +
        "<h3>" + esc(surface.label) + "</h3>" + inner + "</section>";
    }).join("");
    if (!sections) sections = '<div class="help-ref-empty">No tooltips match &ldquo;' + esc(query) + "&rdquo;.</div>";
    return { html: sections, count: count };
  }

  // The whole panel (header + search + scrolling body + footer). Pure -> a string.
  //
  // THE DEPENDENCY-FREE FALLBACKS ARE GONE. Each of the three ("if DWFUI failed to load, hand-build
  // the header / the search box / the scroll box") was a second, drifting copy of markup DWFUI owns
  // -- precisely drift rule R2 -- and the hand-built close control inside the header fallback was
  // this file's last raw form control. They were also DEAD: DWFUI is loaded before this module in
  // index.html, and under node it is require()d at the top of this file. dwf-unitcycle.js
  // already set this precedent ("DWFUI loads first in index.html; no hand-rolled fallback").
  //
  // The panel shell KEEPS its ARIA dialog role, and that is reported rather than migrated: the
  // builder which owns that role is windowHtml, but windowHtml stamps `dwfui-window info-window` onto
  // the element, and `.info-window` carries `height:100%` plus its own gold inset frame -- which
  // would stretch this panel and draw a SECOND border over `.hk-panel`'s own. web/css/dwf.css
  // is locked this wave, so that collision cannot be reconciled here; adopting it needs the CSS
  // consolidation wave.
  function render(corpus, curated, query) {
    var body = renderBody(corpus, curated, query);
    var header = DWFUI.headerHtml({
      cls: "hk-head help-ref-head",
      titleTag: "h2", title: "Help — all tooltips",
      tools: '<span class="help-ref-count">' + body.count + " entries</span>",
      close: { cls: "hk-close", data: "help-close", title: "Close" },
    });
    var search = DWFUI.searchHtml({ cls: "help-ref-search", inputCls: "help-ref-search-input", magnifier: true,
      dataAttr: "help-search", value: query || "", placeholder: "Search every tooltip, guide, and shortcut…" });
    var scrollInner = '<div class="help-ref-scroll-inner" data-help-body>' + body.html + "</div>";
    var scroll = DWFUI.scrollHtml({ cls: "help-ref-body" }, scrollInner);
    return '<div class="hk-panel help-ref-panel" role="dialog" aria-label="Help reference">' +
      header + search + scroll +
      '<div class="hk-foot help-ref-foot">Every tooltip the game shows lives here. ' +
      "Press <b>Shift+H</b> for the compact hotkey card, or <b>Esc</b> to close.</div></div>";
  }

  // ---- browser glue --------------------------------------------------------------------------

  var overlayEl = null;
  var open = false;

  function corpus() { return root.DFHelpCorpus || (typeof module !== "undefined" && { surfaces: [] }); }
  function curated() { return root.DFHelpCurated || { notes: {} }; }

  function ensureOverlay() {
    if (overlayEl) return overlayEl;
    if (typeof document === "undefined") return null;
    overlayEl = document.getElementById("helpReference");
    if (!overlayEl) {
      overlayEl = document.createElement("div");
      overlayEl.id = "helpReference";
      document.body.appendChild(overlayEl);
    }
    // Backdrop click closes -- bound ONCE on the persistent container (paint() replaces innerHTML,
    // so per-render bindings on children die with their nodes, but this one must not accumulate).
    overlayEl.addEventListener("pointerdown", function (ev) { if (ev.target === overlayEl) close(); });
    return overlayEl;
  }

  function paint(query) {
    var el = ensureOverlay();
    if (!el) return;
    el.innerHTML = render(corpus(), curated(), query || "");
    el.querySelector("[data-help-close]") &&
      el.querySelector("[data-help-close]").addEventListener("click", close);
    var input = el.querySelector("[data-help-search]");
    if (input) {
      input.addEventListener("input", function () { repaintBody(input.value); });
    }
  }

  // Re-render only the scrolling body + the count on each keystroke, so the search input keeps
  // focus and caret position (the caret-restore idiom the info shells use).
  function repaintBody(query) {
    var el = ensureOverlay();
    if (!el) return;
    var body = renderBody(corpus(), curated(), query || "");
    var host = el.querySelector("[data-help-body]");
    if (host) host.innerHTML = body.html;
    var count = el.querySelector(".help-ref-count");
    if (count) count.textContent = body.count + " entries";
  }

  function openPanel() {
    var el = ensureOverlay();
    if (!el) return;
    paint("");
    el.classList.add("open");
    open = true;
    var input = el.querySelector("[data-help-search]");
    if (input) { try { input.focus(); } catch (_) {} }
  }
  function close() {
    if (overlayEl) overlayEl.classList.remove("open");
    open = false;
    try { document.getElementById("view") && document.getElementById("view").focus({ preventScroll: true }); } catch (_) {}
  }
  function toggle() { if (open) close(); else openPanel(); }
  function isOpen() { return open; }

  if (typeof document !== "undefined") {
    document.addEventListener("keydown", function (ev) {
      if (ev.key === "Escape" && open) { ev.preventDefault(); close(); }
    });
    // The top-right "?" button and the ?/F1 keys are wired in dwf-controls-placement.js (it
    // owns the topbar cluster and uses stopImmediatePropagation to keep one handler per button);
    // both call window.DFHelpPanel.toggle(). This module deliberately does NOT also bind #helpBtn,
    // so the button can never double-toggle.
  }

  root.DFHelpPanel = { render: render, renderBody: renderBody, open: openPanel, close: close, toggle: toggle, isOpen: isOpen };
  if (typeof module !== "undefined" && module.exports) {
    module.exports = { render: render, renderBody: renderBody, entryMatches: entryMatches, curatedNote: curatedNote };
  }
})(typeof window !== "undefined" ? window : (typeof globalThis !== "undefined" ? globalThis : this));
